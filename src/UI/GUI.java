    private boolean DoesValueGoOverThreshold(String inputValue) {
        if (inputValue.length() == 0) {
            return false;
        }
        Double value = Double.parseDouble(inputValue);
        if (!Metrique_Button.isSelected()) {
            value *= 2.54;
        }
        return (value > Etagere.MAX_SIZE);
    }
